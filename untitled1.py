# ════════════════════════════════════════════════════════════════════
#  1.  RL ENVIRONMENT
#
#  Goal: allocate limited ICU beds to patients, prioritising those
#        with the highest severity scores.
#
#  State:  list of patients with id, severity (1-10), arrival type
#  Action: patient id to assign a bed to
#  Reward: +severity (+2 Emergency bonus, +1 Urgent bonus)
#          -5  if patient id does not exist
#          -10 if no beds remain
#  Episode ends when all beds filled OR all patients served.
# ════════════════════════════════════════════════════════════════════

class HospitalEnv:
    def __init__(self, n_beds: int = 5, n_patients: int = 10):
        self.n_beds = n_beds
        self.n_patients = n_patients
        self.beds_left = n_beds
        self.patients: list[dict] = []
        self.total_reward = 0.0
        self.steps_taken = 0
        self.done = False

    def reset(self) -> dict:
        self.beds_left = self.n_beds
        self.total_reward = 0.0
        self.steps_taken = 0
        self.done = False
        self.patients = [
            {
                "id": i,
                "severity": round(random.uniform(1.0, 10.0), 1),
                "name": f"Patient-{i:02d}",
                "arrival": random.choice(["Emergency", "Urgent", "Standard"]),
            }
            for i in range(self.n_patients)
        ]
        return {
            "available_beds": self.beds_left,
            "total_patients": self.n_patients,
            "patients": list(self.patients),
        }

    def step(self, action: int) -> dict:
        if self.done:
            return {
                "reward": 0, "remaining_beds": self.beds_left,
                "done": True, "patients_left": list(self.patients),
                "total_reward": round(self.total_reward, 2),
                "steps": self.steps_taken,
                "error": "Episode is done. Please reset.",
            }

        if self.beds_left <= 0:
            self.done = True
            return {
                "reward": -10, "remaining_beds": 0,
                "done": True, "patients_left": list(self.patients),
                "total_reward": round(self.total_reward, 2),
                "steps": self.steps_taken,
                "error": "No beds remaining.",
            }

        patient = next((p for p in self.patients if p["id"] == action), None)
        if patient is None:
            reward = -5.0
            self.total_reward += reward
            self.steps_taken += 1
            return {
                "reward": reward, "remaining_beds": self.beds_left,
                "done": False, "patients_left": list(self.patients),
                "total_reward": round(self.total_reward, 2),
                "steps": self.steps_taken,
                "error": f"Patient {action} not found.",
            }

        reward = patient["severity"]
        if patient["arrival"] == "Emergency":
            reward += 2.0
        elif patient["arrival"] == "Urgent":
            reward += 1.0
        reward = round(reward, 2)

        self.total_reward = round(self.total_reward + reward, 2)
        self.patients = [p for p in self.patients if p["id"] != action]
        self.beds_left -= 1
        self.steps_taken += 1
        self.done = (self.beds_left == 0) or (len(self.patients) == 0)

        return {
            "reward": reward,
            "remaining_beds": self.beds_left,
            "done": self.done,
            "patients_left": list(self.patients),
            "total_reward": self.total_reward,
            "steps": self.steps_taken,
            "assigned_patient": patient,
        }


# ════════════════════════════════════════════════════════════════════
#  2.  FASTAPI BACKEND
# ════════════════════════════════════════════════════════════════════

app = FastAPI(title="Hospital Bed Allocation API")
env = HospitalEnv(n_beds=5, n_patients=10)


@app.get("/health")
def health():
    return {"status": "ok"}

@app.get("/reset")
def reset():
    return env.reset()

@app.post("/step")
def step(action: int):
    return env.step(action)


def run_server():
    uvicorn.run(app, host="127.0.0.1", port=8000, log_level="error")


# ════════════════════════════════════════════════════════════════════
#  3.  START BACKEND IN BACKGROUND THREAD
# ════════════════════════════════════════════════════════════════════

def wait_for_backend(retries=30, delay=0.4):
    for _ in range(retries):
        try:
            if requests.get("http://127.0.0.1:8000/health", timeout=1).status_code == 200:
                return True
        except Exception:
            pass
        time.sleep(delay)
    return False


print("⏳ Starting backend server...")
threading.Thread(target=run_server, daemon=True).start()

if wait_for_backend():
    print("✅ Backend ready at http://127.0.0.1:8000")
else:
    print("❌ Backend failed to start — check for port conflicts")


