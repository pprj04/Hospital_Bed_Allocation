BASE_URL = "http://127.0.0.1:8000"
patient_table_data: list[list] = []


def reset_env():
    global patient_table_data
    try:
        res = requests.get(f"{BASE_URL}/reset", timeout=5).json()
    except Exception as e:
        return (f"❌ Backend error: {e}", [], "—", "—", "—", "—")

    patient_table_data = [
        [p["id"], p["name"], p["severity"], p["arrival"], "⏳ Pending"]
        for p in res.get("patients", [])
    ]
    return (
        "✅ New episode started. Assign beds using the Patient IDs shown below.",
        patient_table_data,
        str(res.get("available_beds", "—")),
        str(res.get("total_patients", "—")),
        "0",
        "0.00",
    )


def assign_bed(patient_id_str: str):
    global patient_table_data

    if not patient_id_str.strip():
        return ("⚠️ Enter a Patient ID first.", patient_table_data,
                gr.update(), gr.update(), gr.update(), gr.update())
    try:
        patient_id = int(patient_id_str.strip())
    except ValueError:
        return ("⚠️ Patient ID must be a whole number.", patient_table_data,
                gr.update(), gr.update(), gr.update(), gr.update())

    try:
        res = requests.post(
            f"{BASE_URL}/step", params={"action": patient_id}, timeout=5
        ).json()
    except Exception as e:
        return (f"❌ Backend error: {e}", patient_table_data,
                gr.update(), gr.update(), gr.update(), gr.update())

    if "error" in res:
        return (f"⚠️ {res['error']}", patient_table_data,
                gr.update(), gr.update(), gr.update(), gr.update())

    reward        = res.get("reward", 0)
    remaining     = res.get("remaining_beds", "—")
    done          = res.get("done", False)
    patients_left = res.get("patients_left", [])
    total_reward  = res.get("total_reward", 0)
    assigned      = res.get("assigned_patient", {})

    for row in patient_table_data:
        if row[0] == patient_id:
            row[4] = "✅ Assigned"
            break

    assigned_count = sum(1 for r in patient_table_data if r[4] == "✅ Assigned")
    icon = "🟢" if reward > 0 else "🔴"
    arrival = assigned.get("arrival", "")
    episode_note = "  🏁 Episode complete — press Reset to start again." if done else ""
    log = (
        f"{icon}  Patient {patient_id} ({arrival}) assigned  "
        f"|  Reward: +{reward}  |  Beds left: {remaining}{episode_note}"
    )

    return (
        log,
        list(patient_table_data),
        str(remaining),
        str(len(patients_left)),
        str(assigned_count),
        str(total_reward),
    )


